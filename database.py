# CONNECTION HANDLING
# ---------------------------------------------------------------------
@contextmanager
def get_connection():
    """
    Yields a sqlite3 connection configured with row access by column
    name (sqlite3.Row). Commits on clean exit, rolls back on exception,
    always closes.
    """
    conn = sqlite3.connect(str(DATABASE_PATH), timeout=SQLITE_TIMEOUT_SECONDS)
    conn.row_factory = sqlite3.Row
    try:
        yield conn
        conn.commit()
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()


def _safe_list(fn):
    """Decorator: any read function that should return a List[Dict] returns [] on error instead of raising."""
    @wraps(fn)
    def wrapper(*args, **kwargs):
        try:
            return fn(*args, **kwargs)
        except Exception as e:
            logger.error(f"{fn.__name__} failed, returning [] ({e})")
            return []
    return wrapper


def _safe_dict(fn):
    """Decorator: any read function that should return a Dict returns {} on error instead of raising."""
    @wraps(fn)
    def wrapper(*args, **kwargs):
        try:
            return fn(*args, **kwargs)
        except Exception as e:
            logger.error(f"{fn.__name__} failed, returning {{}} ({e})")
            return {}
    return wrapper


def _safe_write(default=None):
    """Decorator: any write function logs and returns `default` instead of raising, so the UI never crashes."""
    def decorator(fn):
        @wraps(fn)
        def wrapper(*args, **kwargs):
            try:
                return fn(*args, **kwargs)
            except Exception as e:
                logger.error(f"{fn.__name__} failed ({e})")
                return default
        return wrapper
    return decorator


