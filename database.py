@_safe_write(default=0)
def insert_detections(
    detections: List[Dict],
    road_id: Optional[int] = None,
    session_id: Optional[str] = None,
    source_type: str = "image",
    source_path: Optional[str] = None,
    annotated_path: Optional[str] = None,
    source_filename: Optional[str] = None,
    asset_type: Optional[str] = None,
) -> int:
    """
    Bulk-inserts one row per detection dict. Detection dicts follow the
    detection_service contract: {class_name, confidence, area_px,
    bbox: (x1,y1,x2,y2), frame_number?, timestamp_sec?}.

    Returns the number of rows inserted.
    """
    if not detections:
        return 0

    with get_connection() as conn:
        road_name = _road_name_for(conn, road_id)
        rows = []
        for d in detections:
            bbox = d.get("bbox") or (None, None, None, None)
            rows.append((
                road_id, road_name, session_id, asset_type, source_type,
                source_path, source_filename, annotated_path,
                d.get("class_name", "unknown"), d.get("confidence", 0.0), d.get("area_px", 0.0),
                bbox[0], bbox[1], bbox[2], bbox[3],
                d.get("frame_number"), d.get("timestamp_sec"),
            ))
        conn.executemany(
            """INSERT INTO detections
               (road_id, road_name, session_id, asset_type, source_type,
                source_path, source_filename, annotated_path,
                class_name, confidence, area_px, bbox_x1, bbox_y1, bbox_x2, bbox_y2,
                frame_number, timestamp_sec)
               VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)""",
            rows,
        )
    logger.info(f"Inserted {len(rows)} detections (road_id={road_id}, session_id={session_id})")
    return len(rows)


@_safe_list
def fetch_all_detections(limit: int = 1000) -> List[Dict]:
    with get_connection() as conn:
        rows = conn.execute(
            "SELECT * FROM detections ORDER BY created_at DESC LIMIT ?", (limit,)
        ).fetchall()
    return _rows_to_list(rows)


@_safe_list
def fetch_detections_by_session(session_id: str) -> List[Dict]:
    with get_connection() as conn:
        rows = conn.execute(
            "SELECT * FROM detections WHERE session_id = ? ORDER BY frame_number, detection_id", (session_id,)
        ).fetchall()
    return _rows_to_list(rows)


@_safe_list
def get_detection_history(road_id: Optional[int] = None, limit: int = 1000) -> List[Dict]:
    """
    All detections, most recent first. If road_id is given, filtered to
    that road/flyover only; if None, returns across every asset (used by
    the Analytics Dashboard's all-time overview).
    """
    query = "SELECT * FROM detections"
    params: tuple = ()
    if road_id is not None:
        query += " WHERE road_id = ?"
        params = (road_id,)
    query += " ORDER BY created_at DESC LIMIT ?"
    params = params + (limit,)
    with get_connection() as conn:
        rows = conn.execute(query, params).fetchall()
    return _rows_to_list(rows)


