@_safe_dict
def get_email_settings() -> Dict:
    """
    Returns the single, app-wide email settings row:
        {"receiver_email": str, "auto_send_enabled": bool, "updated_at": str}
    Defaults to an empty/disabled config if never saved before.
    """
    with get_connection() as conn:
        row = conn.execute("SELECT * FROM email_settings WHERE id = 1").fetchone()
    if not row:
        return {"receiver_email": "", "auto_send_enabled": False, "updated_at": None}
    d = _row_to_dict(row)
    d["auto_send_enabled"] = bool(d.get("auto_send_enabled"))
    return d


@_safe_write(default=False)
def save_email_settings(receiver_email: str, auto_send_enabled: bool) -> bool:
    """Upserts the single email_settings row (id is always 1 - one config for the whole app)."""
    with get_connection() as conn:
        conn.execute(
            """INSERT INTO email_settings (id, receiver_email, auto_send_enabled, updated_at)
               VALUES (1, ?, ?, CURRENT_TIMESTAMP)
               ON CONFLICT(id) DO UPDATE SET
                 receiver_email = excluded.receiver_email,
                 auto_send_enabled = excluded.auto_send_enabled,
                 updated_at = CURRENT_TIMESTAMP""",
            (receiver_email, bool(auto_send_enabled)),
        )
    logger.info(f"Email settings saved (receiver={receiver_email}, auto_send={auto_send_enabled})")
    return True


