# SCHEMA INITIALIZATION + AUTO-MIGRATION
# ---------------------------------------------------------------------
def _existing_columns(conn: sqlite3.Connection, table: str) -> set:
    rows = conn.execute(f"PRAGMA table_info({table})").fetchall()
    return {r["name"] for r in rows}


def _migrate_schema(conn: sqlite3.Connection) -> None:
    """
    Self-heals older database files: for every table/column this module
    expects, adds any column that's missing from the actual on-disk
    schema. This is what prevents `sqlite3.OperationalError: no such
    column: ...` when a DB created by an older version of this app is
    reused (e.g. one missing `road_id` or `road_name`).
    """
    for table, expected_cols in _EXPECTED_COLUMNS.items():
        try:
            current_cols = _existing_columns(conn, table)
        except sqlite3.OperationalError:
            # Table itself doesn't exist yet - CREATE TABLE IF NOT EXISTS
            # above should have created it; if not, skip (nothing to migrate).
            continue

        for col, col_def in expected_cols.items():
            if col in current_cols:
                continue
            if "PRIMARY KEY" in col_def.upper():
                # Can't ALTER TABLE ADD a PRIMARY KEY column after the fact.
                continue
            try:
                # ALTER TABLE ADD COLUMN doesn't accept UNIQUE/CHECK - strip
                # to a bare type + optional DEFAULT for migration safety.
                safe_def = col_def.split("CHECK(")[0].strip()
                conn.execute(f"ALTER TABLE {table} ADD COLUMN {col} {safe_def}")
                logger.info(f"Migrated schema: added {table}.{col} ({safe_def})")
            except sqlite3.OperationalError as e:
                logger.warning(f"Could not add column {table}.{col}: {e}")


def init_db() -> None:
    """
    Creates all tables/indexes if they don't already exist, then
    auto-migrates any pre-existing (older-schema) database file.
    Safe to call on every app startup; never raises.
    """
    try:
        with get_connection() as conn:
            conn.executescript(SCHEMA_SQL)
            _migrate_schema(conn)
        logger.info(f"Database initialized at {DATABASE_PATH}")
    except Exception as e:
        logger.error(f"init_db() failed: {e}")


def _row_to_dict(row: Optional[sqlite3.Row]) -> Dict:
    return dict(row) if row is not None else {}


def _rows_to_list(rows: List[sqlite3.Row]) -> List[Dict]:
    return [dict(r) for r in rows]


