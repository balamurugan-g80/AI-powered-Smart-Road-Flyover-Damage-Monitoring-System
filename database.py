@_safe_list
def list_roads() -> List[Dict]:
    """All road/flyover assets, most recently created first."""
    with get_connection() as conn:
        rows = conn.execute(
            "SELECT road_id, name, asset_type, created_at FROM road_assets ORDER BY created_at DESC"
        ).fetchall()
    return _rows_to_list(rows)


# Alias requested by spec - identical to list_roads().
def list_assets() -> List[Dict]:
    return list_roads()


@_safe_write(default=None)
def get_or_create_road(name: str, asset_type: str = "road") -> Optional[int]:
    """
    Returns the road_id for `name`, creating a new road_assets row if one
    doesn't already exist with that name. Existing rows are NOT
    overwritten (asset_type on an existing name is left as-is) - use
    update_road() to change an existing asset's type.
    """
    name = (name or "").strip() or "Unnamed Segment"
    with get_connection() as conn:
        row = conn.execute("SELECT road_id FROM road_assets WHERE name = ?", (name,)).fetchone()
        if row:
            return row["road_id"]
        cursor = conn.execute(
            "INSERT INTO road_assets (name, asset_type) VALUES (?, ?)", (name, asset_type or "road"),
        )
        return cursor.lastrowid


def create_road(name: str, asset_type: str = "road") -> Optional[int]:
    """Explicit create - same get-or-create semantics as get_or_create_road()."""
    return get_or_create_road(name, asset_type)


@_safe_dict
def get_road(road_id: int) -> Dict:
    with get_connection() as conn:
        row = conn.execute(
            "SELECT road_id, name, asset_type, created_at FROM road_assets WHERE road_id = ?", (road_id,)
        ).fetchone()
    return _row_to_dict(row)


@_safe_write(default=False)
def update_road(road_id: int, name: Optional[str] = None, asset_type: Optional[str] = None) -> bool:
    if name is None and asset_type is None:
        return False
    fields, params = [], []
    if name is not None:
        fields.append("name = ?")
        params.append(name)
    if asset_type is not None:
        fields.append("asset_type = ?")
        params.append(asset_type)
    params.append(road_id)
    with get_connection() as conn:
        conn.execute(f"UPDATE road_assets SET {', '.join(fields)} WHERE road_id = ?", params)
    return True


@_safe_write(default=False)
def delete_road(road_id: int) -> bool:
    """Deletes a road asset. Historical detections/predictions/etc keep their
    road_id/road_name values (soft-orphaned) rather than being cascade-deleted,
    so past inspection history is never silently lost."""
    with get_connection() as conn:
        conn.execute("DELETE FROM road_assets WHERE road_id = ?", (road_id,))
    return True


def _road_name_for(conn: sqlite3.Connection, road_id: Optional[int]) -> Optional[str]:
    """Internal helper: looks up a road's name for the denormalized road_name column."""
    if road_id is None:
        return None
    row = conn.execute("SELECT name FROM road_assets WHERE road_id = ?", (road_id,)).fetchone()
    return row["name"] if row else None


