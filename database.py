"""
database.py
------------
SQLite persistence layer for the Smart Road & Flyover Damage
Monitoring & Decision Intelligence System.

Owns ALL SQLite access for the Streamlit app - no other module should
open a connection directly.

SIX tables:

    road_assets   - one row per named road/flyover segment (id, name, type)
    detections    - every individual YOLOv8 finding (image or video frame)
    predictions   - one row per analysis session: health/risk/emergency/
                    repair-cost/RUL/recommendation, i.e. the full
                    decision-intelligence output for that session
    notifications - every alert dispatched by notification_service
    complaints     - every municipal complaint generated by complaint_service
    reports        - any exported report/PDF/CSV file reference

Design notes
------------
* road_id (an integer FK into road_assets) is the PRIMARY key app.py uses
  for every lookup/filter. A denormalized `road_name` column is also kept
  on every table for convenience/back-compat with older, name-keyed
  callers - it costs nothing and avoids a join for simple listing pages.
* road_id may be NULL: the sidebar lets a user run detection against
  "(New road/flyover)" before clicking "Create asset", so every write
  function accepts road_id=None gracefully.
* No FOREIGN KEY constraints are declared. This is deliberate: per spec,
  "No database errors should ever crash the app" - a strict FK constraint
  would raise on any orphaned/None reference, which is exactly the
  failure mode we're avoiding here. Referential integrity is maintained
  at the application layer instead.
* Every read function returns [] / {} / None on any error instead of
  raising, and logs the exception - see `_safe_list` / `_safe_dict`.
* `init_db()` is idempotent and additionally self-heals older database
  files: after CREATE TABLE IF NOT EXISTS, `_migrate_schema()` inspects
  each table's actual columns via PRAGMA table_info and ALTER TABLE ADD
  COLUMN's anything missing (e.g. an old DB created before `road_id`
  existed). This is what prevents the classic
  `sqlite3.OperationalError: no such column: ...` crash on startup.
* Connections are opened per-call (not held open) since Streamlit reruns
  the whole script on every interaction - this avoids "database is
  locked" / cross-thread sqlite3 errors without needing a connection pool.
"""

